    subroutine set_strickler_spatial_fields(msh, iseg, x, strickler_params, interp)
        implicit none
        type(Mesh), intent(inout) :: msh
        integer(ip), intent(in):: iseg
        real(rp), dimension(:), intent(in) :: x
