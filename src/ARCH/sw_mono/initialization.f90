   SUBROUTINE read_qlatfiles(msh)
   
   !===================================================================================================================!
   ! Global variables
   !===================================================================================================================!

   type(Mesh), intent(in   )  ::  msh

   !===================================================================================================================!
   ! Local variables
   !===================================================================================================================!
   
   character(len=lchar) :: filename
   integer :: loc
   real :: x
   integer :: times, space, nbtimesim

   !===================================================================================================================  
   ! Read t and Q from Qlat hydrographs
   !===================================================================================================================

    space = size(bc%hyd_lat%x)
    times = size(bc%hyd_lat%t)

    do i=1,space
!Spatial loop
      filename = trim(bc%hyd_lat%id( i )) // '.txt'

      open(13,file=filename,status='old')
        read(13,*)
        read(13,*)
        read(13,*)
        read(13,*) x
        
        bc%hyd_lat%x( i ) = x
        
        read(13,*) times
          
       !!! Read t, Q values
       do j = 1,times
!Time loop
          read(13,*) bc%hyd_lat%t( j ), bc%hyd_lat%q( i,j )
       end do 
!End time loop

      close(13)
      
    end do
!End spatial loop

    !===================================================================================================================  
    ! Calculate location of the inflows on the spatial grid (x in meters from bc%hyd_lat%x become cell indexes in bc%hyd_lat%loc
    !===================================================================================================================
    do i = 1,space

!Spatial loop
      loc = minloc(abs(msh%cs%coord%x - bc%hyd_lat%x( i )), 1) !!Find index of xsim closest to xdata (equivalent to ie in the euler_time_step_first scheme)

      !!! Change x to correspondong x in msh
      if (bc%hyd_lat%x( i ) > msh%cs(loc)%coord%x) then
        bc%hyd_lat%x( i ) = msh%cs(loc+1)%coord%x
      else
        bc%hyd_lat%x( i ) = msh%cs(loc)%coord%x
      end if
      call AddToList(bc%hyd_lat%loc, loc)!Add loc value to unallocated list 

    end do 
!End spatial loop


    !===================================================================================================================  
    ! Initiate dof%qlat
    !===================================================================================================================

      dof%qlat(1,:)=bc%hyd_lat%q(:,1)
      dof%qlat(2,:)=dof%qlat(1,:)
      
!       print*,bc%hyd_lat%t
!        print*,bc%hyd_lat%x
!         print*,bc%hyd_lat%loc
!          print*,bc%hyd_lat%q
!           print*,dof%qlat


   END SUBROUTINE read_qlatfiles
   
