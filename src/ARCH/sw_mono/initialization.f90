   ! ================================================
   ! Compute Manning parameters at each cross-section
   ! ================================================
   if (friction /= 0) then
      call read_strickler_params(msh)   ! read parameters defining the Strickler coeff from input file
      call calc_K_params_everywhere(msh)
   end if
   
   !=====================================================================================================================!
   ! Re computation of elevation of gost cell
   !=====================================================================================================================!
   sumDis=msh%cs(3)%delta
   do i = 4,msh%ncs+2
      sumDis=sumDis+msh%cs(i)%delta
   enddo
      


   slopemean=-(msh%cs(msh%ncs+2)%bathy-msh%cs(3)%bathy)/sumDis

   !Fill gost cell (W_boundary)
   msh%cs(1)%bathy=msh%cs(3)%bathy+msh%cs(4)%deltademi*slopemean*2._rp
   msh%cs(2)%bathy=msh%cs(3)%bathy+msh%cs(4)%deltademi*slopemean

   do j=1,msh%cs(3)%nlevels
         msh%cs(1)%height(j)=msh%cs(3)%height(j)+msh%cs(4)%deltademi*slopemean*2_rp
         msh%cs(2)%height(j)=msh%cs(3)%height(j)+msh%cs(4)%deltademi*slopemean
   end do

   !Fill gost cell (E_boundary)
   msh%cs(msh%ncs+3)%bathy=msh%cs(msh%ncs+2)%bathy-msh%cs(msh%ncs+1)%deltademi*slopemean
   msh%cs(msh%ncs+4)%bathy=msh%cs(msh%ncs+2)%bathy-msh%cs(msh%ncs+1)%deltademi*slopemean*2._rp

   do j=1,msh%cs(msh%ncs+2)%nlevels
         msh%cs(msh%ncs+3)%height(j)=msh%cs(msh%ncs+2)%height(j)+&
         msh%cs(msh%ncs+1)%deltademi*slopemean
