      yR=(msh%cs(i+1)%coord%y+msh%cs(i)%coord%y)*demi

      msh%cs(i)%delta=sqrt((xR-xL)**2+(yR-yL)**2)
   end do

   do i=1,3
      msh%cs(i)%delta=msh%cs(4)%delta
   end do
   do i=msh%ncs+2,msh%ncs+4
      msh%cs(i)%delta=msh%cs(msh%ncs+1)%delta
   end do

   !===================================================================================================================!
   !  Deltademi x calculation msh%cs(i)%deltademi=distance(xi-1,xi)
   !===================================================================================================================!
   do i = 4,msh%ncs+2
      xL=msh%cs(i-1)%coord%x
      yL=msh%cs(i-1)%coord%y
      xR=msh%cs(i)%coord%x
      yR=msh%cs(i)%coord%y

      msh%cs(i)%deltademi=sqrt((xR-xL)**2+(yR-yL)**2)
   end do

   do i=1,3
      msh%cs(i)%deltademi=msh%cs(4)%deltademi
   end do
   do i=msh%ncs+2,msh%ncs+4
      msh%cs(i)%deltademi=msh%cs(msh%ncs+1)%deltademi
